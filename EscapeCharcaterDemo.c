/* Escape character demo */

#include<stdio.h>

int main()
{

puts("Newline and backslash use");
printf(" Hello \n Dan \\Hope your are able to hear me\n"); //newline and backslash character
puts("Tab character");
printf("\n Hello Dan \tHope your are able to hear me\n"); //tab character
puts("Escape single  charcater");
printf("\n Hello Dan \'Hope you are good \n"); // Escape single quote
puts("Escape double quote");
printf("\n Hello Dan \"Hope you are good"); //Escape double quote
return(0);
}
